#include "ft_fdf.h"

void	draw_row_edges(t_fdf *fdf, t_map_row *row, int crow);
void	draw_column_edges(t_fdf *fdf, t_map_row *row, int crow);
void	draw_colour_diagonals(t_fdf *fdf, t_map_row *row, int crow);

/**
 * The XSync function flushes the output buffer and then waits until
 * all requests have been received and processed by the X server.
 * Any errors generated must be handled by the error handler.
 * For each protocol error received by Xlib, XSync calls the client
 * application's error handling routine (see section 11.8.2).
 *
 * Any events generated by the server are enqueued into the library's
 * event queue.
 */
void	on_expose(t_fdf *fdf)
{
	t_map_row	*row;
	int			cr;

	row = fdf->map;
	cr = fdf->rows - 1;
	while (row)
	{
		draw_row_edges(fdf, row, cr);
		draw_column_edges(fdf, row, cr);
		if (fdf->custom_colour_flag)
			draw_colour_diagonals(fdf, row, cr);
		row = row->next;
		cr--;
	}
	mlx_put_image_to_window(fdf->mlx, fdf->root,
		fdf->canvas, fdf->offset.x, fdf->offset.y);
}

void	draw_row_edges(t_fdf *fdf, t_map_row *row, int crow)
{
	int	cc;

	cc = fdf->cols;
	while (--cc > 0)
		draw_line_d(fdf, get_point(fdf, row, cc, crow),
			get_point(fdf, row, cc - 1, crow));
}

void	draw_column_edges(t_fdf *fdf, t_map_row *row, int crow)
{
	int	cc;

	if (!row->next)
		return ;
	cc = fdf->cols;
	while (cc-- > 0)
		draw_line_d(fdf, get_point(fdf, row, cc, crow),
			get_point(fdf, row->next, cc, crow - 1));
}

void	draw_colour_diagonals(t_fdf *fdf, t_map_row *row, int crow)
{
	int	cc;

	if (!row->next)
		return ;
	cc = fdf->cols;
	while (--cc > 0)
		draw_line_d(fdf, get_point(fdf, row, cc, crow),
			get_point(fdf, row->next, cc - 1, crow - 1));
}
